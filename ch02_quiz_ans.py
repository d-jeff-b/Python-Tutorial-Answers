# 1. 設定屬性：召喚兩位高手
# 建立變數 attack，由「千面幻術師 (int)」負責盛裝整數能量
attack = 50 

# 建立變數 buff，由「半點禪師 (float)」負責盛裝帶有小數點的加成能量
buff = 1.5 


# 2. 計算傷害：屬性大亂鬥
# 當 int 與 float 相乘時，結果會被禪師同化，自動轉化為 float 屬性
total_damage = attack * buff 


# 3. 終極顯影：施展 f-string 大調和陣法
# 透過 f 標記與 {} 花括號，我們不需要施展 str() 轉化術也能直接融合能量
# 變數會直接在字串中顯影，呈現最直覺的戰報
print(f"「俠客目前的基礎攻擊為 {attack}，加成後最終傷害值為：{total_damage}」")