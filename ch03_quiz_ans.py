# --- 第三章：群集的奧祕 —— 四大幫派大點兵解答 ---

# 1. 清單幫 (list)：特性是「有序、可變動」。
# 使用中括號 [] 建立背包，原本有索引 0 和 1 的物品
bag = ["木劍", "小藥水"] 

# 使用 .append() 咒語動態新增物資，"地圖" 會被排在索引 2 的位置
bag.append("地圖") 


# 2. 元組幫 (tuple)：特性是「有序、不可變動」。
# 使用小括號 () 刻下鐵律，這份名單一旦建立就無法被修改
laws = ("不准斷網", "不准熬夜") 

# 利用索引 [0] 取得第一條規矩
first_law = laws[0]


# 3. 字典幫 (dict)：特性是「鍵值對 (Key-Value)」。
# 使用大括號 {} 配合冒號 : 建立狀態庫，透過「暗號」精準取值
status = {"等級": 5, "HP": 100}


# 4. 集合幫 (set)：特性是「無序、元素唯一」。
# 使用大括號 {} 建立技能組，雖然輸入了兩次 "冥想"，
# 但「淨化結界」會自動移除重複殘影，只保留一個真身。
skills = {"冥想", "劍術", "冥想"}


# 5. 終極顯影：使用 f-string 大調和陣法
# 這裡展示了如何同時引用不同幫派的資料：
# - status['等級']：從字典取出數值
# - bag[2]：從清單取出剛加入的地圖
# - laws[0]：從元組取出第一條規矩
# - skills：顯示淨化後的集合成果
print(f"「等級 {status['等級']} 的俠客帶著 {bag[2]}，正遵守著 {laws[0]} 在修煉 {skills}。」")